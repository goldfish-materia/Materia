Peter Knøss
Jeg er lærer i bl.a. dansk og sløjd og har arbejdet med dette gennem mange år. Desuden har jeg en Pædagogisk diplomuddannelse i sløjd og en cand. pæd i materiel kultur. Gennem tolv år har jeg arbejdet som lektor på læreruddannelsen i Århus, men valgte i 2013 at foretage et karriereskifte. I dag arbejder jeg som skoleleder på Randers Lilleskole.
