<p>Hvem vi er
Kursuscentret Materia, ejes og drives af skoleleder Peter Engelbrekt Knøss fra Randers Lilleskole, samt hans kone Gitte Lautrup, der arbejder som adjunkt i gymnasieskolen. Her foruden er løst tilknyttet en række personer med stærke kompetencer på forskellige felter.
<br>
Navnet Materia
Navnet Materia er inspireret af filosoffen Aristoteles´ tanker om hvordan verden er opbygget. Materia var en betegnelse for uformet stof og på Materia former vi materialer.
Materia kaldes også Materialernes Verden fordi der arbejdes med mange materialer. Da Materia ligger på Kristrup Engvej, omtales stedet også som dette.
<br>
Vores historie
For 20 år siden startede vi som en kursusvirksomhed, som kørte rundt i Danmark og lavede kurser i smedning, grov smedning, dukkebygning og bronzestøbning.
I 2005 købte vi 23.000m2 jord ved Randers, som nu er vores faste undervisningssted. Her er smedje, 2 pavilloner, huggehus, bål hus, shelter, 3 huse i lille skov og toiletter.
<br>
Kursusstedets undervisere er de dygtigste på hver sit felt, det betyder, at alle kurser er på et meget højt niveau. Kursusstedet har tilknyttet 5 forskellige undervisere, men det er Peter der sørger for den daglige drift.
