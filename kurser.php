Mad i det fri (1 dag). Pris 400Kr.
Dato: 12. 5. 2012. Kl. 9-19
Mad i det fri, er kurset som vil give dig en indføring i madkunst over bål. Der vil både blive kogt, stegt, bagt og røget over åben ild. Endvidere skal vi fremstille og afprøve den årtusinde gamle opfindelse, jordovnen. Så hvis du er lærer eller pædagog som gerne vil i gang med at lave mad sammen med børn i det fri, eller privatperson som godt kunne tænke sig at komme i gang med at lave mad over bål hjemme i haven, så er dette et kursus som vil få dig godt i gang.
Fakta: Materia råder over Danmarks måske største åbne grill…
<br>
Stenhugning  (2 dage) Pris 1000Kr.
Dato: 29-30.10. 2011. Begge dage 9-16 (husk varmt tøj).
Kurset i stenhugning er for dig der synes granit er et smukt materiale, -men som endnu ikke ved, hvordan man bearbejder det. Kurset vil give dig indsigt i materialer, forskellige værktøjer og maskiner samt slibning, pudsning og polering. Desuden vil der på kurset blive diskuteret formgivning og blive givet råd og vejledning til de enkelte projekter.
Kræver ingen forkundskaber, men det er en fordel, hvis du ikke er bange for at lære en vinkelsliber at kende.
<br>
Bronzestøbning - sandstøbning (2 dage) Pris 800Kr.
Dato: 08-09.2011. Begge dage 9-16
Kurset er for dig der gerne vil prøve at udtrykke dig i et helt andet materiale, nemlig bronze. På kurset vil du lære om formmateriale, temperaturer, sikkerhed, opbygning af formkasse, støbning og færdigbearbejdning af din skulptur/genstand. Der vil også indgå forskellige patineringsteknikker i kurset.
