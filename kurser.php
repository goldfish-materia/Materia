Kreativitet, innovation og entreprenørskab (3 dage) Pris: 1500Kr.
Dato: 25-27.5. 2015. Fredag 16-21. Lørdag og søndag 9-16
Alle steder fra peges på behovet for innovation! Men hvordan lærer vi at blive innovative, og hvad vil det egentlig sige? – en ny måde at tænke og handle på?
Kurset vil tage udgangspunkt i forskellige innovationsteorier samt anvende KIE modellen som metode for de mange workshops som deltagerne vil blive præsenteret for i løbet af kurset.
Kurset vil endvidere sætte fokus på at diskutere, rammesætte og teoretisere begreberne kreativitet, innovation og entreprenørskab.
(Kurset kan også laves på andre tidspunkter for større grupper)
NB.: Kurset vil begynde med en mindre hjemmeopgave som forberedelse
